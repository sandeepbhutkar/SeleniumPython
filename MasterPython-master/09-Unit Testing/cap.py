# we are going to write very simple method which capitalize text
def cap_text(text):
    '''
    This is very simple method to capitalize a string
    :param text: input a string
    :return: capitalized string
    '''
    return text.capitalize()

# we are going to write one more very simple method which capitalize first letter of multiple words
def cap_multiple(text):
    '''
    This is very simple method to capitalize a string
    :param text: input a string
    :return: capitalized string
    '''
    return text.title()
