# Equality check
print(2 == 2) #True

print(2 == 1) #False

#comparing strings
print('bye' == 'Bye') #False

print(2.0 == 2) #True

# Not equal to
print(4 != 5)

# Greater or less than
print(2 > 1) #True

print(2 < 1) #False

# Chaining comparision operators with logical operators
# we are going to use logical operators AND OR NOT
print(1 < 2 and 2 > 3) #Fasle condition at left and right of and should be true

print(1 == 1 or 2 == 2) #True one condition either left ot right should be true

# Not is used when you want opposite of the condition
print(not 1 == 1) # 1 == 1 is Ture but not will return False


